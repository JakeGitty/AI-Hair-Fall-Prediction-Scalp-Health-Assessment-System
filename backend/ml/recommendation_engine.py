import json

def generate_health_recommendation(probability_score: float, features: dict = None) -> dict:
    """
    Generates a structured clinical recommendation based on hair fall risk probability.
    
    Args:
        probability_score (float): The model's risk prediction (0.0 to 100.0).
        features (dict, optional): Selected user inputs (e.g., {"stress": "High"}) 
                                   to further personalize recommendations.
                                   
    Returns:
        dict: A JSON-serializable dictionary containing the risk analysis.
    """
    
    # Base Categorization Math
    if probability_score <= 30.0:
        risk_level = "Low Risk"
        explanation = "Your scalp and hair follicles appear extremely healthy with no significant indicators of premature hair fall. The analysis detected strong density and minimal thinning."
        recommendations = [
            "Maintain your current balanced diet rich in vitamins and minerals.",
            "Continue getting proper, consistent sleep.",
            "Avoid overly aggressive chemical hair treatments to protect your roots."
        ]
    
    elif probability_score <= 60.0:
        risk_level = "Moderate Risk"
        explanation = "The AI detected early signs of potential hair thinning or scalp distress. This is common and often highly reversible when addressed early."
        recommendations = [
            "Increase intake of proteins and iron-rich foods.",
            "Adopt stress-reduction techniques (e.g., mindfulness, exercise).",
            "Ensure proper scalp hygiene using sulfate-free, gentle shampoos.",
            "Consider over-the-counter topical solutions like 2% Minoxidil (consult a professional first)."
        ]
        
    else:
        risk_level = "High Risk"
        explanation = "The analysis strongly indicates advanced hair thinning, follicle miniaturization, or severe scalp inflammation. Immediate action is recommended to halt or reverse progression."
        recommendations = [
            "Schedule a consultation with a certified dermatologist immediately.",
            "Seek a formal medical diagnosis for underlying conditions (e.g., Alopecia, Thyroid issues).",
            "Discuss FDA-approved prescription treatments (e.g., Finasteride, 5% Minoxidil) with your doctor.",
            "Avoid wearing tight hats or hairstyles that cause traction alopecia."
        ]

    # Add dynamic personalization if features are present
    if features:
        if features.get('stress_level') == 'High':
            recommendations.insert(0, "CRITICAL: Your high stress levels are a major catalyst. Prioritize cortisol reduction.")
        if features.get('sleep_hours', 8) < 6:
            recommendations.append("Increase your sleep to 7-8 hours to allow cellular follicle repair.")

    return {
        "risk_percentage": round(probability_score, 1),
        "risk_level": risk_level,
        "explanation": explanation,
        "recommendations": recommendations,
        "disclaimer": "This assessment is generated by an AI model and is for informational purposes only. It is not a medical diagnosis. Always consult a healthcare professional for serious concerns."
    }

# Example usage for testing
if __name__ == "__main__":
    result = generate_health_recommendation(45.2, {"stress_level": "High"})
    print(json.dumps(result, indent=2))
